            comboFonts.SelectedIndex = 0;
        }

        private void btnDecode_Click(object sender, EventArgs e)
        {
            Font font = (Font)comboFonts.SelectedItem;

            Image img = FontCoder.Decode(font.Data, font.Width, font.Height);

            if (img != null)
            {
                pic.Image = img;
            }
            else
            {
                MessageBox.Show("No image decoded!", "Error", MessageBoxButtons.OK, MessageBoxIcon.Exclamation);
            }
        }
